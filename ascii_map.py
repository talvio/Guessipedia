def map_pos_to_row_col(pos, left_longitude, top_latitude, rows, cols):
    """
    Imagine a world map in a coordinate system where origo is at the top left corner
    and y axis runs down, not up.
    If you give this function a tuple lat, lon, where 0, 0 is at the equator,
    Greenwich meridian, it will map it to the world map coordinate system
    row and columns.
    :param pos: lat, lon tuple
    :param left_longitude: left edge longitude of the ascii map
    :param top_latitude: top edge latitude of the ascii map
    :param rows: how many rows in the ascii map
    :param cols: how many columns in the ascii map
    :return: map the lat, lon to the ascii world map
    """
    lat = pos[0]
    lon = pos[1]
    y = top_latitude - lat  # This is y if the map height is 180 rows
    x = lon - left_longitude # This is the column, if the map width is 360 columns
    y = round((y / 180) * rows)
    x = round((x / 360) * cols)
    return y, x # In this order because lat, lon order


def map_locations(positions, left_longitude=LEFT_LONGITUDE, top_latitude=TOP_LATITUDE, ascii_world=WORLD):
    """
    Function takes the world map in ASCII and positions the tuples representing lat, lon pairs from the
    argument positions
    :param positions: A list of lat, lon tuples.
    :param left_longitude: What longitude is the left edge of the ASCII map
    :param top_latitude:   What latitude is the top of the map
    :param ascii_world:    Ascii world map art
    :return:               A multi line string where the positions are placed as * characters in red
    """
    world_as_list = ascii_world.split("\n")
    world_width = max(len(line) for line in world_as_list)
    world_height = len(world_as_list)
    world = []
    for line_no, line in enumerate(world_as_list):
        world.append(list(line))
    for num, pos in enumerate(positions):
        lat, lon = map_pos_to_row_col(pos, left_longitude, top_latitude, world_height, world_width)
        if lat >= len(world) or lat < 0 or lon >= len(world[lat]) or lon < 0:
            world.append(f"{RED}{pos_to_txt(pos)} is outside our ASCII map{RESET_STYLE}")
            continue
        if num == 0:
            world[lat][lon] = f"{BRIGHT_GREEN}*{RESET_STYLE}"
        else:
            world[lat][lon] = f"{RED}*{RESET_STYLE}"
    world_string = ""
    for line in world:
        world_string += ''.join(line) + "\n"
    return world_string


